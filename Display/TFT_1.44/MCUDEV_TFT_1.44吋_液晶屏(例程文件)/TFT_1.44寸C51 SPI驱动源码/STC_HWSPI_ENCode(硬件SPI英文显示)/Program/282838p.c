//========================================================================
// 函数: void	SPI_init(void)
// 描述: 配置硬件SPI，可通过修改SPEED_4值来更改SPI总线速度
// 参数: none.
// 返回: none.
// 版本: VER1.0
// 日期: 2012-12-15
// 备注: SPEED_4为4分频最高速，SPEED_128为最低速
//========================================================================
////
void	SPI_init(void)	
{
	SPCTL = (SSIG << 7) + (SPEN << 6) + (DORD << 5) + (MSTR << 4) + (CPOL << 3) + (CPHA << 2) + SPEED_4;
}

//========================================================================
// 函数: void  delay_ms(unsigned char ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数.
// 返回: none.
// 版本: VER1.0
// 日期: 2012-12-15
// 备注: 
//========================================================================
void  delay_ms(unsigned char ms)
{
     unsigned int i;
	 do{
	      i = MAIN_Fosc / 14000;
		  while(--i)	;   //14T per loop
     }while(--ms);
}
//向SPI总线传输一个8位数据
void SPI_WriteByte(uchar byte)
{			
			SPDAT = byte;		//发送一个字节
			while((SPSTAT & SPIF) == 0)	;	//等待发送完成
			SPSTAT = SPIF + WCOL;			//清0 SPIF和WCOL标志
}
//向液晶屏写一个8位指令
void  Lcd_WriteIndex(uchar c)
{
	cs=0;
	rs=0;
	SPI_WriteByte(c);
	cs=1;    
}
//向液晶屏写一个8位数据
void  Lcd_WriteData(uchar d)
{
	cs=0;
	rs=1;
	SPI_WriteByte(d);
	cs=1;
}
//向液晶屏写一个16位数据
void LCD_WriteData_16Bit(int Data)
{
	cs=0;
	rs=1;
	SPI_WriteByte(Data>>8);
	SPI_WriteByte(Data);
	cs=1;
	
}

void Reset()
{
    reset=0;
    delay_ms(100);
    reset=1;
    delay_ms(100);
}
