#include "Led.h"
#include "Delay.h"
#include "stm32f10x_gpio.h"
/**************************************************************************************
上电后系统有个默认时钟启动(先可以不配置 一般配置在72Mh)
****************************************************************************************/
static void InitSys(void)
{
}
/***************************************************************************************/
static void InitBSP(void)
{
   GpioConfigurationLed();
}
/***************************************************************************************/
int main(void)
{
    InitSys();
    InitBSP();

	for(;;) {


	   GPIOC ->ODR =   GPIO_Pin_13;	//熄灭(LED共阳极)
	  
	   DelayLED(0x54321);

	   GPIOC ->ODR &= ~GPIO_Pin_13 ;	//点亮(LED共阳极)

	   DelayLED(0x54321);
	}
}
