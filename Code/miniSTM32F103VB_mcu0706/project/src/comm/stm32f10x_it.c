/*******************************************************************************
* Function Name  : TIM3_IRQHandler
* Description    : This function handles TIM3 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/

static vu16 TIM3_Capture = 0 ;

void TIM3_IRQHandler(void)
{
	
	if (TIM_GetITStatus(KEY_TIMER , TIM_IT_CC_KEY) != RESET)
	{
	  
              #if KEY_EN> 0
		/*按键定时扫描处理程序*/	
		KeyScanBack();
		#endif
				
		 /* Clear KEY_TIMER Capture Compare1 interrupt pending bit*/
		 TIM_ClearITPendingBit(KEY_TIMER, TIM_IT_CC_KEY);
		 TIM3_Capture = TIM_GetCapture1(KEY_TIMER) ;
		TIM_SetCompare1(KEY_TIMER,  TIM3_Capture + TIM_CCR_VAl_KEY_SCAN);
		
	}	
	else if (TIM_GetITStatus(POS_TIMER, TIM_IT_CC_POS) != RESET)
	{
	    	
		#if SERVO_POS_CTR_EN> 0	
		MC_PosCarBack();	
		#endif
		
		/* Clear KEY_TIMER Capture Compare2 interrupt pending bit*/
	  	TIM_ClearITPendingBit(POS_TIMER, TIM_IT_CC_POS);
		TIM3_Capture = TIM_GetCapture2(POS_TIMER) ;
		TIM_SetCompare2(POS_TIMER,  TIM3_Capture + TIM_CCR_VAl_SYS_POS_CTR);

	}
	else if (TIM_GetITStatus(PM_TIMER, TIM_IT_CC_PM) != RESET)
	{

		#if LOAD_PAPER_EN>0
		Load_PaperSensorSamplingBack();
		#endif

		/* Clear KEY_TIMER Capture Compare2 interrupt pending bit*/
	  	TIM_ClearITPendingBit(PM_TIMER, TIM_IT_CC_PM);
		TIM3_Capture = TIM_GetCapture3(PM_TIMER) ;
		TIM_SetCompare3(PM_TIMER,  TIM3_Capture + TIM_CCR_VAl_P_MOTOR_SENSOR_SAMPLEING);

	}
	
}

